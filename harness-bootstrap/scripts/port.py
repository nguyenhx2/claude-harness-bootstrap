def port(target: pathlib.Path, tool: str) -> None:
    claude = target / ".claude"
    rules = claude / "rules"
    hooks = claude / "hooks"
    if not rules.is_dir():
        raise SystemExit(f"no {rules} - scaffold the Claude harness first (run harness-bootstrap).")
    ext = "sh" if (hooks / "protect-secrets.sh").is_file() else "ps1"

    if tool in ("cursor", "all"):
        r = port_cursor_rules(rules, target / ".cursor" / "rules")
        f = port_cursor_hooks(target / ".cursor" / "hooks")
        print(f"\nCursor:")
        print(f"  .cursor/rules/      {len(r)} rules (.md -> .mdc, paths: -> globs:)")
        print(f"  .cursor/hooks.json  registers the beforeShellExecution / beforeReadFile adapter")
        print(f"  {f[1].relative_to(target).as_posix()}  the adapter (Cursor payload -> harness hooks)")
        print(f"  note: Cursor's afterFileEdit is observational, so an edit to an Accepted ADR is")
        print(f"        flagged after the fact, not blocked before. Everything else blocks pre-hoc.")

    if tool in ("codex", "all"):
        h = port_codex_hooks(hooks, target / ".codex", ext)
        print(f"\nCodex:")
        print(f"  AGENTS.md           already generated by harness-bootstrap - Codex reads it natively")
        print(f"  {h.relative_to(target).as_posix()}   registers the existing hooks (Codex payload == Claude payload)")
        print(f"  note: Codex routes file edits through apply_patch (path inside tool_input.command),")
        print(f"        so protect-adr is best-effort there; the Bash-based guards are exact.")


def main() -> int:
    ap = argparse.ArgumentParser(description="Port a scaffolded harness to Cursor or Codex.")
    ap.add_argument("--target", type=pathlib.Path, help="repo that already has a scaffolded .claude/")
    ap.add_argument("--tool", choices=["cursor", "codex", "all"], default="all")
    ap.add_argument("--self-test", action="store_true", help="test the adapter on sample payloads")
    a = ap.parse_args()

    if a.self_test:
        print("Cursor adapter self-test:")
        return self_test()
    if not a.target:
        ap.error("--target is required unless --self-test")
    port(a.target.resolve(), a.tool)
    return 0


if __name__ == "__main__":
    sys.exit(main())
